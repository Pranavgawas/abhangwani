// Utility functions
export const getAllContent = (language = 'english') => {
  const langContent = content[language];
  if (!langContent) return getAllContent('english'); // fallback to English
  return [...langContent.abhangs, ...langContent.aartis];
};

export const getContentByType = (language = 'english', type) => {
  const langContent = content[language];
  if (!langContent) return getContentByType('english', type); // fallback to English
  if (type === 'abhang') return langContent.abhangs;
  if (type === 'aarti') return langContent.aartis;
  return getAllContent(language);
};

export const getContentById = (id) => {
  const allContent = [...getAllContent('english'), ...getAllContent('marathi'), ...getAllContent('hindi')];
  return allContent.find(item => item.id === id);
};

export const searchContent = (query, language = 'english') => {
  const allContent = getAllContent(language);
  return allContent.filter(item => 
    item.saint?.toLowerCase().includes(query.toLowerCase()) ||
    item.deity?.toLowerCase().includes(query.toLowerCase()) ||
    item.firstLine?.toLowerCase().includes(query.toLowerCase()) ||
    item.title?.toLowerCase().includes(query.toLowerCase()) ||
    item.meaning?.toLowerCase().includes(query.toLowerCase())
  );
};